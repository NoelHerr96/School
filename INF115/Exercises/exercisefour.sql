# Task 1 
A = {1, 2, 3, 4}
B = {3, 4, 5, 6}
C = {2, 4, 6, 7}

A x B
= {(1,3), (1,4), (1,5), (1,6), 
(2,3), (2,4), (2,5), (2,6), 
(3,3), (3,4), (3,5), (3,6), 
(4,3), (4,4), (4,5), (4,6)}

A ∪ B, A ∩ B
A ∪ B = {1, 2, 3, 4} ∪ {3, 4, 5, 6} = {1, 2, 3, 4, 5, 6}
A ∩ B = {1, 2, 3, 4} ∩ {3, 4, 5, 6} = {3, 4}

A − B, B − A
A - B = {1, 2, 3, 4} - {3, 4, 5, 6} = {1, 2}
B - A = {3, 4, 5, 6} - {1, 2, 3, 4} = {4, 5}

(A − B) − C, A − (B − C)
(A - B) - C = ({1, 2, 3, 4} - {3, 4, 5, 6}) - {2, 4, 6, 7} = {1, 2} - {2, 4, 6, 7} = {1}
A − (B − C) = {1, 2, 3, 4} - ({3, 4, 5, 6} - {2, 4, 6, 7}) = {1, 2, 3, 4} - {3, 5} = {1, 2, 4}

